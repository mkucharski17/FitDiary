package com.example.android.fitdiary.Day.TrainingDay;

public class Set {
    int reps;
    float weight;

    public Set(int reps, float weight) {
        this.reps = reps;
        this.weight = weight;
    }

    public int getReps() {
        return reps;
    }

    public void setReps(int reps) {
        this.reps = reps;
    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }
}
